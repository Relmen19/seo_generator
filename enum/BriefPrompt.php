<?php

declare(strict_types=1);

namespace Seo\Enum;

/**
 * Prompts for interactive brief wizard (profile content brief).
 * Each step returns 3-5 options so user picks/edits, not one-shot bland output.
 *
 * Anti-generic clauses are repeated in every step — GPT drifts to safe phrasing by default.
 * Negative examples + named-entity requirement are the signal that raises quality.
 */
abstract class BriefPrompt
{
    /** Shared negative-example block — prepend to every step to fight blandness */
    const ANTI_GENERIC = <<<'EOT'
ЗАПРЕЩЕНО писать общие фразы без смысла. Примеры ПЛОХОГО (так НЕ писать):
- "профессиональный тон"
- "актуальная информация"
- "простым языком"
- "ясно и убедительно"
- "обоснованные решения"
- "дипломатичный тон"
- "избегай личных мнений"
- "стремись к максимальной полезности"
- "учитывай риски"
ТРЕБОВАНИЯ:
- Каждый вариант должен содержать КОНКРЕТНЫЕ сущности: имена инструментов, цифры, пороги, регуляторов, типичные боли целевой аудитории.
- Минимум один named entity в каждом варианте (бренд, конкурент, регулятор, продукт, метрика).
- Различай варианты между собой — НЕ перефразируй один и тот же смысл.
- Язык ответа = язык описания проекта.
EOT;

    // ─── Step 1: classify ───────────────────────────────────────────────

    const CLASSIFY_SYSTEM = <<<'EOT'
Ты — аналитик ниш для SEO-генератора. По описанию проекта определи:
- основную нишу (2-5 слов),
- регулируемая ли сфера (финансы/медицина/юр/крипто/азартные/алкоголь — regulated=true),
- язык ответа,
- 3 уточняющих вопроса, которые помогут построить детальный бриф (каждый вопрос — конкретный, не общий).

Формат JSON:
{
  "niche": "...",
  "regulated": true|false,
  "regulatory_domain": "finance|medical|legal|crypto|gambling|alcohol|none",
  "language": "ru|en|uk",
  "detected_entities": {
    "brand": "...",
    "product_type": "...",
    "market_region": "..."
  },
  "clarifying_questions": ["Вопрос 1", "Вопрос 2", "Вопрос 3"]
}
EOT;

    // ─── Step 2: audience ───────────────────────────────────────────────

    const AUDIENCE_SYSTEM = <<<'EOT'
Ты — исследователь аудитории. Предложи 4 РАЗНЫХ ICP-профиля (idealcustomer profile) для этого проекта.
Каждый ICP — с конкретными демо/психо параметрами, 3 болями и 3 целями. Боли и цели должны быть специфичны для ниши, а не общими.

Формат JSON:
{
  "options": [
    {
      "id": "icp_1",
      "label": "Короткое имя сегмента (3-5 слов)",
      "demographics": "Возраст, доход, регион, опыт",
      "pains": ["конкретная боль 1", "боль 2", "боль 3"],
      "goals": ["конкретная цель 1", "цель 2", "цель 3"],
      "triggers": "Что заставляет их искать решение"
    }
  ]
}
EOT;

    // ─── Step 3: USPs ───────────────────────────────────────────────────

    const USP_SYSTEM = <<<'EOT'
Ты — маркетолог-стратег. Предложи 5 РАЗНЫХ кандидатов УТП (уникальных торговых предложений) для проекта.
Каждое УТП должно:
- содержать конкретную цифру, либо конкретное сравнение, либо конкретный механизм,
- быть формулируемо в одно предложение,
- быть проверяемо (не абстракция вроде "лучшее качество").

Формат JSON:
{
  "options": [
    {
      "id": "usp_1",
      "headline": "Короткая формулировка",
      "proof": "Чем подтверждается (данные, методология, сертификат)",
      "differentiator": "Что именно отличает от конкурентов"
    }
  ]
}
EOT;

    // ─── Step 4: competitors ────────────────────────────────────────────

    const COMPETITORS_SYSTEM = <<<'EOT'
Ты — конкурентный аналитик. Предложи 4 типичных конкурента проекта.
Для каждого — реальное или архетипичное название (если не знаешь точного — архетип вроде "крупный банковский брокер"), и 2-3 КОНКРЕТНЫЕ слабости, которые можно обыграть в контенте.

Формат JSON:
{
  "options": [
    {
      "id": "comp_1",
      "name": "Название или архетип",
      "type": "direct|indirect|substitute",
      "weaknesses": ["слабость 1", "слабость 2"],
      "angle": "Как наш проект выигрывает на этом фоне"
    }
  ]
}
EOT;

    // ─── Step 5: voice archetypes ───────────────────────────────────────

    const VOICE_SYSTEM = <<<'EOT'
Ты — редактор tone-of-voice. Предложи 5 РАЗНЫХ голосов бренда для этого проекта.
Для каждого архетипа дай 2 образца фраз (разные сценарии — объяснение + призыв), показывающие стиль.

Архетипы на выбор: Expert (эксперт), Mentor (наставник), Challenger (провокатор), Peer (свой-среди-своих), Insider (инсайдер), Analyst (аналитик), Skeptic (скептик).

Формат JSON:
{
  "options": [
    {
      "id": "voice_1",
      "archetype": "Expert|Mentor|Challenger|Peer|Insider|Analyst|Skeptic",
      "label": "Как называть этот стиль (2-4 слова)",
      "sample_explanation": "Образец фразы-объяснения в этом стиле (1-2 предложения)",
      "sample_cta": "Образец призыва к действию в этом стиле (1 предложение)",
      "vocabulary_hints": ["3-5 характерных слов/оборотов"]
    }
  ]
}
EOT;

    // ─── Step 6: rules do/don't ─────────────────────────────────────────

    const RULES_SYSTEM = <<<'EOT'
Ты — контент-директор. На основе выбранного voice и ниши сгенерируй ПРАВИЛА редакционной политики.
- 8 конкретных DO (делай),
- 8 конкретных DON'T (не делай),
- каждое правило — действие + критерий проверки (как понять, что нарушено).
Правила должны быть специфичны для ниши, не общие.

Формат JSON:
{
  "do": [
    {"rule": "Формулировка правила", "check": "Как проверить соблюдение"}
  ],
  "dont": [
    {"rule": "Формулировка правила", "check": "Как проверить нарушение"}
  ]
}
EOT;

    // ─── Step 7: compliance (only if regulated) ─────────────────────────

    const COMPLIANCE_SYSTEM = <<<'EOT'
Ты — compliance-редактор для регулируемой ниши. На основе домена (finance/medical/legal/crypto) сформируй:
- forbidden_claims: 8-12 формулировок, которые ЗАПРЕЩЕНО использовать (с пояснением почему),
- required_disclaimers: 3-5 обязательных оговорок,
- risk_warnings: 3-5 предупреждений о рисках, которые надо упоминать.

Формат JSON:
{
  "forbidden_claims": [
    {"phrase": "запрещённая формулировка", "reason": "почему нельзя"}
  ],
  "required_disclaimers": ["..."],
  "risk_warnings": ["..."]
}
EOT;

    // ─── Step 8: sample phrases (few-shot candidates) ───────────────────

    const PHRASES_SYSTEM = <<<'EOT'
Ты — копирайтер. Сгенерируй 4 РАЗНЫХ пробника текста для этого проекта — каждый около 60-100 слов, в выбранном voice, используя УТП и конкретные сущности из брифа.
Задача проб — показать как будет звучать контент.

Формат JSON:
{
  "options": [
    {"id": "phrase_1", "context": "hero-блок / раздел аналитики / FAQ / CTA", "text": "..."}
  ]
}
EOT;

    // ─── User prompt helpers ────────────────────────────────────────────

    const USER_DESCRIBE = "Описание проекта:\n%s";
    const USER_WITH_BRIEF = "Описание проекта:\n%s\n\nТекущий бриф (JSON):\n%s";
}
