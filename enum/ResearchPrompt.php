<?php

declare(strict_types=1);

namespace Seo\Enum;

/**
 * Prompts for article research dossier generation.
 * Output is markdown with fixed sections — semantic, not block-bound.
 * Used as factual base for meta + blocks generation later.
 */
abstract class ResearchPrompt
{
    const SYSTEM = "Ты — исследователь-аналитик. Собираешь досье фактов для последующего написания статьи."
        . " Пишешь на русском. Ответ — ТОЛЬКО markdown по заданной структуре, без преамбул и комментариев."
        . "\n\nПравила:"
        . "\n1. Каждый факт — конкретный, с цифрами/механизмом/источником, без воды и общих фраз."
        . "\n2. Если факт нельзя подтвердить — пиши `— unknown` в этом пункте, НЕ выдумывай."
        . "\n3. Каждый пункт self-check: относится ли он к Углу? Если нет — выкинь."
        . "\n4. Не используй маркетинговые штампы («революционный», «уникальный», «современный»)."
        . "\n5. Цифры всегда с единицей и условиями (что, сколько, при каких условиях)."
        . "\n6. Сравнения — только по измеримым осям."
        . "\n7. Не повторяйся между секциями.";

    /** Skeleton — модель должна заполнить КАЖДУЮ секцию, минимумы соблюдать. */
    const SKELETON = <<<MD
# Research dossier: {TITLE}

## Угол
(1-2 предложения. Конкретный вопрос статьи. Якорь — всё дальше держится за это.)

## Аудитория и intent
(Кто читает, что хочет узнать, на каком уровне подготовки.)

## Ключевые сущности
(Минимум 3. Формат: `- Название — что это, 1 строка`)

## Факты
(Минимум 10. Формат: `- Тезис. Обоснование/механизм.`)

## Цифры и бенчмарки
(Минимум 5. Формат: `- Метрика: значение единица, условия. (источник если есть)`)

## Сравнения
(Минимум 2. Формат: `- X vs Y по [ось1, ось2]: X=..., Y=...`)

## Тезисы и контр-тезисы
(Минимум 3. Формат: `- Тезис. Возражение/когда не работает.`)

## Цитаты и кейсы
(Минимум 2. Цитата с автором ИЛИ конкретный кейс «компания X сделала Y → результат Z». Если нет — `— unknown`.)

## Термины
(Минимум 3. Формат: `- Термин — определение`)

## Открытые вопросы
(Что нельзя утверждать без проверки. Минимум 2.)

## Источники
(URL/референсы если использовались. Иначе `— unknown`.)
MD;

    const USER_TEMPLATE = "Тема статьи: «%s»\n%s%s\nЗаполни досье ниже строго по структуре, соблюдая минимумы и правила.\n\n%s";
}
