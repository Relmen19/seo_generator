<?php

declare(strict_types=1);

namespace Seo\Enum;

/**
 * Prompts for template generation, review, and proposal.
 * Sources: TemplateGeneratorService
 */
abstract class TemplatePrompt
{
    /** Architect role for single-template generation */
    const ARCHITECT_ROLE = "Ты — архитектор SEO-шаблонов. Твоя задача — спроектировать ОДИН шаблон статьи для конкретного типа контента.";

    /** Anti-generic guard — prepended to system prompts to force concreteness. */
    const ANTI_GENERIC = <<<'EOT'
КАЧЕСТВО ПРОМПТОВ — КРИТИЧНО. Промпты и hint'ы должны быть КОНКРЕТНЫМИ, не общими.

ЗАПРЕЩЕНО использовать общие безличные формулировки. Плохие примеры (так НЕ писать):
- "профессиональный и дипломатичный тон"
- "актуальная информация по теме"
- "объясняй сложные концепции простым языком"
- "стремись к максимальной полезности для читателя"
- "учитывай риски и потенциальные выгоды"
- "избегай личных мнений, основывайся на фактах"
- "предоставляй точные и актуальные советы"
- "помоги читателю принять обоснованное решение"

ОБЯЗАТЕЛЬНО в каждом hint и в gpt_system_prompt:
- Упоминать КОНКРЕТНЫЕ сущности из брифа: бренд, имена конкурентов, названия УТП, боли аудитории, метрики.
- Указывать, какие цифры/факты должны фигурировать (тип данных, источник).
- Ссылаться на voice-архетип и характерные обороты.
- Если есть compliance — дословно перечислять запрещённые формулировки и обязательные оговорки.
- Для сравнительных блоков — называть конкретных конкурентов по имени, если они в брифе.
- Никаких абстрактных «профессионально», «качественно», «подробно» — только проверяемые требования.
EOT;

    /** Rules for single-template generation */
    const SINGLE_RULES = <<<'EOT'
ПРАВИЛА:
1. Шаблон ДОЛЖЕН начинаться с `hero` и заканчиваться `cta`.
2. Используй 5-10 блоков в зависимости от сложности типа статьи.
3. gpt_system_prompt — ПОДРОБНАЯ инструкция для GPT (5-8 предложений), обязана:
   - назвать целевой ICP и его ключевые боли из брифа;
   - перечислить 2-3 УТП, которые нужно обыгрывать;
   - задать voice-архетип и 2-3 характерных оборота;
   - перечислить запрещённые формулировки compliance, если есть;
   - указать типы допустимых источников данных.
4. hint для каждого блока — конкретная подсказка (3-5 предложений) с:
   - точным описанием контента блока в контексте назначения шаблона,
   - упоминанием минимум одного named entity (бренд / конкурент / метрика / регулятор),
   - указанием тональности и длины.
5. description шаблона — не пересказ назначения общими словами, а 2-3 предложения: для какой аудитории, какая цель, чем отличается от других шаблонов профиля.
6. Подбирай блоки, максимально подходящие для данного типа статьи. НЕ ставь блоки «для галочки».
7. Имена, описания и промпты — на языке проекта (обычно русский).
8. slug — латиницей, через дефис.
EOT;

    /** JSON response format for single-template generation */
    const SINGLE_RESPONSE_FORMAT = <<<'EOT'
ФОРМАТ ОТВЕТА (строго JSON):
{
  "template": {
    "name": "Название шаблона",
    "slug": "template-slug",
    "description": "Описание назначения шаблона — для какого типа статей",
    "css_class": "tpl-slug",
    "gpt_system_prompt": "Подробный системный промпт для GPT при генерации статьи по этому шаблону...",
    "blocks": [
      {
        "type": "hero",
        "name": "Название блока",
        "hint": "Детальная подсказка GPT для генерации этого блока",
        "fields": ["title", "subtitle"],
        "sort_order": 1,
        "is_required": true
      }
    ]
  }
}
EOT;

    /** Rules for multi-template proposal */
    const PROPOSAL_RULES = <<<'EOT'
ПРАВИЛА:
1. Каждый шаблон ДОЛЖЕН начинаться с `hero` и заканчиваться `cta`.
2. Используй 5-8 блоков на шаблон.
3. Каждый шаблон УНИКАЛЕН по структуре И по назначению — НЕ дублируй типы статей.
4. gpt_system_prompt — инструкция (4-6 предложений) с named entities из брифа: бренд, конкуренты, УТП, voice, compliance-запреты.
5. hint каждого блока (3-5 предложений) — конкретика: что писать, какие цифры, каких конкурентов называть, какие боли закрывать. НЕ общими словами.
6. description шаблона — 2 предложения: для какого ICP и какой цели, чем отличается от других шаблонов.
7. Имена, описания и промпты — на языке проекта.
8. slug — латиницей, через дефис.
EOT;

    /** JSON response format for multi-template proposal */
    const PROPOSAL_RESPONSE_FORMAT = <<<'EOT'
ФОРМАТ ОТВЕТА (строго JSON):
{
  "templates": [
    {
      "name": "Название шаблона",
      "slug": "template-slug",
      "description": "Описание назначения шаблона",
      "css_class": "tpl-slug",
      "gpt_system_prompt": "Системный промпт для GPT...",
      "blocks": [
        {
          "type": "hero",
          "name": "Название блока",
          "hint": "Подсказка GPT для генерации этого блока",
          "fields": ["title", "subtitle"],
          "sort_order": 1,
          "is_required": true
        }
      ]
    }
  ]
}
EOT;

    /** Reviewer role */
    const REVIEWER_ROLE = "Ты — ревьюер SEO-шаблонов. Проверь качество шаблона и предложи улучшения.";

    /** Review evaluation criteria */
    const REVIEW_CRITERIA = <<<'EOT'
КРИТЕРИИ ОЦЕНКИ:
1. Логичность структуры блоков (порядок, необходимость каждого блока)
2. Качество gpt_system_prompt (достаточно ли подробный, учитывает ли нишу)
3. Качество hint для каждого блока (конкретность, полезность для GPT)
4. Покрытие нужных аспектов типа статьи
5. Нет ли лишних или недостающих блоков
EOT;

    /** JSON response format for review */
    const REVIEW_RESPONSE_FORMAT = <<<'EOT'
ФОРМАТ ОТВЕТА (строго JSON):
{
  "score": 8,
  "suggestions": ["Замечание 1", "Замечание 2"],
  "improved_template": {
    "name": "...",
    "slug": "...",
    "description": "...",
    "css_class": "...",
    "gpt_system_prompt": "Улучшенный промпт...",
    "blocks": [...]
  }
}
EOT;

    /** Review closing instruction */
    const REVIEW_INSTRUCTION = "Если шаблон хорош — поставь высокую оценку и верни его без изменений в improved_template.\nЕсли есть что улучшить — улучши и верни исправленную версию.";

    /** User prompt: create template for purpose. %s = purpose */
    const USER_CREATE_TEMPLATE = "Создай шаблон для следующего типа статьи: %s";

    /** User prompt: pick suitable blocks */
    const USER_PICK_BLOCKS = 'Подбери блоки, которые лучше всего подходят для этого типа контента. Для каждого блока напиши подробный hint.';

    /** User prompt: create N templates. %d = count */
    const USER_PROPOSAL_CREATE = "Создай %d шаблонов статей, подходящих для этой ниши.";

    /** User prompt: coverage instruction */
    const USER_PROPOSAL_COVERAGE = 'Шаблоны должны покрывать разные типы контента: информационный, сравнительный, обзорный, руководство и т.д.';

    /** User prompt: review instruction */
    const USER_DO_REVIEW = 'Проведи ревью шаблона и предложи улучшения.';
}
